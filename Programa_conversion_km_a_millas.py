print("Este es el programa para convertir KM a Millas")
milla = 1.609344
km = float(input("Por favor ingrese la cantidad de KM a convertir a millas: "))
resultado = round(float((km*1)/milla), 5)
print("La conversion de " , km , "Kilometros a millas es: " , resultado)
print("Ya pude hacer un programa de conversiones :D ")
